    <h1>Dott. Andrea Costacurta</h1>
    <img src="../public/images/profile2.jpg" alt="Foto Andrea Costacurta" class="float-lg-left mr-4">
    <div>
        <p class="text-justify pr-5">Tanti anni di esperienza nel campo della psicoterapia hanno continuato ad alimentare l’entusiasmo che provo per il mio lavoro. 
            Il lavoro con le persone, le emozioni ed i sentimenti mi hanno da sempre dato grandi soddisfazioni.
            Ho cominciato a svolgere la mia professione come psicoterapeuta alla fine degli anni ottanta, per poi iscrivermi all’Albo degli Psicologi-Psicoterapeuti dal ’93 (anno in cui l’albo è stato costituito in Italia) con il numero 775 – essere una delle prime persone iscritte all’Albo mi è sempre stato di grande motivazione. 
            In questa professione non esiste routine, ogni persona porta in sè un intero universo e nessuno è uguale all’altro – andare alla scoperta di questo universo, insieme al paziente, è quello che amo della mia professione.
        </p>
